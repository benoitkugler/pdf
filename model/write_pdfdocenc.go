// stringToPDFDocEncoding encode go string `s` to PdfDocEncoding,
// and return true only if all the runes have been encoded
func stringToPDFDocEncoding(s string) ([]byte, bool) {
	var buf bytes.Buffer
	ok := true
	for _, r := range s {
		b, has := PdfDocRunes[r]
		if !has {
			ok = false
			continue
		}
		buf.WriteByte(b)
	}

	return buf.Bytes(), ok
}

